package pack215;

public class quadrant1st4th215
{
	public static void main(String[]args)
	{
		int e=5;
		 for ( int a=1;a<=5;a++)
		  {
		    for ( int b=e-1;b>=a;b--)
		    {
		      System.out.print(" ");
		    }
		    for ( int c=1;c<=a;c++)
		    {
		      System.out.print("*");
		    }
		    System.out.println();
		 }

		 for ( int a=4;a>=1;a--)
		  {
		    for ( int b=e-1;b>=a;b--)
		    {
		      System.out.print(" ");
		    }
		    for ( int c=1;c<=a;c++)
		    {
		      System.out.print("*");
		    }
		    System.out.println();

       	}
	}
}
		
			
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
	

